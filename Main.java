public class Main {

    // Method to perform a task
    public static void greet(String name) {
        System.out.println("Hello, " + name + "!");
    }

    public static void main(String[] args) {
        // Program starts here
        System.out.println("Program started...");

        // Call a method
        greet("Vishnu");

        // End of program
        System.out.println("Program ended.");
    }
}
